/**
 * WORK-IN-PROGRESS: Sample reactive forwarding application using intent framework.
 */
@Component(immediate = true)
public class IntentReactiveForwarding {

    private final Logger log = getLogger(getClass());

    @Reference(cardinality = ReferenceCardinality.MANDATORY_UNARY)
    protected CoreService coreService;

    @Reference(cardinality = ReferenceCardinality.MANDATORY_UNARY)
    protected TopologyService topologyService;

    @Reference(cardinality = ReferenceCardinality.MANDATORY_UNARY)
    protected PacketService packetService;

    @Reference(cardinality = ReferenceCardinality.MANDATORY_UNARY)
    protected IntentService intentService;

    @Reference(cardinality = ReferenceCardinality.MANDATORY_UNARY)
    protected HostService hostService;

    @Reference(cardinality = ReferenceCardinality.MANDATORY_UNARY)
    protected FlowObjectiveService flowObjectiveService;

    private ReactivePacketProcessor processor = new ReactivePacketProcessor();
    private ApplicationId appId;

    private static final int DROP_RULE_TIMEOUT = 300;

    private static final EnumSet<IntentState> WITHDRAWN_STATES = EnumSet.of(IntentState.WITHDRAWN,
                                                                            IntentState.WITHDRAWING,
                                                                            IntentState.WITHDRAW_REQ);

    @Activate
    public void activate() {
        appId = coreService.registerApplication("org.onosproject.ifwd");

        packetService.addProcessor(processor, PacketProcessor.director(2));

        TrafficSelector.Builder selector = DefaultTrafficSelector.builder();
        selector.matchEthType(Ethernet.TYPE_IPV4);
        packetService.requestPackets(selector.build(), PacketPriority.REACTIVE, appId);

        log.info("Started");
    }

    @Deactivate
    public void deactivate() {
        packetService.removeProcessor(processor);
        processor = null;
        log.info("Stopped");
    }

    /**
     * Packet processor responsible for forwarding packets along their paths.
     */
    private class ReactivePacketProcessor implements PacketProcessor {

        @Override
        public void process(PacketContext context) {
            // Stop processing if the packet has been handled, since we
            // can't do any more to it.
            if (context.isHandled()) {
                return;
            }
            InboundPacket pkt = context.inPacket();
            Ethernet ethPkt = pkt.parsed();

            if (ethPkt == null) {
                return;
            }

            HostId srcId = HostId.hostId(ethPkt.getSourceMAC());
            HostId dstId = HostId.hostId(ethPkt.getDestinationMAC());

            // Do we know who this is for? If not, flood and bail.
            Host dst = hostService.getHost(dstId);
            if (dst == null) {
                flood(context);
                return;
            }

            // Otherwise forward and be done with it.
            setUpConnectivity(context, srcId, dstId);
            forwardPacketToDst(context, dst);
        }
    }

    // Floods the specified packet if permissible.
    private void flood(PacketContext context) {
        if (topologyService.isBroadcastPoint(topologyService.currentTopology(),
                                             context.inPacket().receivedFrom())) {
            packetOut(context, PortNumber.FLOOD);
        } else {
            context.block();
        }
    }

    // Sends a packet out the specified port.
    private void packetOut(PacketContext context, PortNumber portNumber) {
        context.treatmentBuilder().setOutput(portNumber);
        context.send();
    }

    private void forwardPacketToDst(PacketContext context, Host dst) {
        TrafficTreatment treatment = DefaultTrafficTreatment.builder().setOutput(dst.location().port()).build();
        OutboundPacket packet = new DefaultOutboundPacket(dst.location().deviceId(),
                                                          treatment, context.inPacket().unparsed());
        packetService.emit(packet);
        log.debug("IntentReactiveForwarding - Forwarding Packet: {}", packet);
    }

    /**
     * Creates a TrafficSelector Builder matching on IPv4's TCP/UDP protocol source and destination ports.
     *
     * @param context The packet context.
     * @return TrafficSelector.Builder
     */
    private TrafficSelector.Builder matchIpProtocolPortTrafficSelector(PacketContext context) {
        TrafficSelector.Builder selectorBuilder = DefaultTrafficSelector.builder();

        InboundPacket pkt = context.inPacket();
        Ethernet ethPkt = pkt.parsed();

        // match on the ethernet protocol
        selectorBuilder.matchEthType(ethPkt.getEtherType());

        if (ethPkt.getEtherType() == Ethernet.TYPE_IPV4) {
            IPv4 ipv4Pkt = (IPv4) ethPkt.getPayload();

            // match on the IP protocol
            selectorBuilder.matchIPProtocol(ipv4Pkt.getProtocol());

            if (ipv4Pkt.getProtocol() == IPv4.PROTOCOL_TCP) {
                TCP tcpPkt = (TCP) ipv4Pkt.getPayload();

                if (tcpPkt.getSourcePort() != 22 && tcpPkt.getDestinationPort() != 22) {
                    // match on the tcp src/dst port
                    selectorBuilder.matchTcpSrc(TpPort.tpPort(tcpPkt.getSourcePort()))
                            .matchTcpDst(TpPort.tpPort(tcpPkt.getDestinationPort()));
                }
            } else if (ipv4Pkt.getProtocol() == IPv4.PROTOCOL_UDP) {
                UDP udpPkt = (UDP) ipv4Pkt.getPayload();

                // match on the udp src/dst port
                selectorBuilder.matchUdpSrc(TpPort.tpPort(udpPkt.getSourcePort()))
                        .matchUdpDst(TpPort.tpPort(udpPkt.getDestinationPort()));
            }
        }
        return selectorBuilder;
    }

    // Install a rule forwarding the packet to the specified port.
    private void setUpConnectivity(PacketContext context, HostId srcId, HostId dstId) {

        // use tcp/udp src and dst from packet as selector
        TrafficSelector selector = matchIpProtocolPortTrafficSelector(context).build();
        TrafficTreatment treatment = DefaultTrafficTreatment.emptyTreatment();

        Key key = HostToHostIntent.createSelectorKey(srcId, dstId, selector, appId);

        // Check if HostToHostIntent has already been submitted
        HostToHostIntent intent = (HostToHostIntent) intentService.getIntent(key);
        // TODO handle the FAILED state
        if (intent != null) {
            if (WITHDRAWN_STATES.contains(intentService.getIntentState(key))) {
                HostToHostIntent hostIntent = HostToHostIntent.builder()
                        .appId(appId)
                        .key(key)
                        .one(srcId)
                        .two(dstId)
                        .selector(selector)
                        .treatment(treatment)
                        .build();

                intentService.submit(hostIntent);
            } else if (intentService.getIntentState(key) == IntentState.FAILED) {

                // only install drop rule, if the same intent is not pending
                if (Sets.newHashSet(intentService.getPending()).stream()
                        .map(i -> i.key())
                        .noneMatch(k -> k.equals(key))) {
                    log.warn("IntentReactiveForwarding: Intent with key {} failed. Install drop rule.", key.toString());

                    TrafficSelector.Builder objectiveSelectorBuilder = DefaultTrafficSelector.builder()
                            .matchEthSrc(srcId.mac()).matchEthDst(dstId.mac());
                    for (Criterion criterion : selector.criteria()) {
                        objectiveSelectorBuilder.add(criterion);
                    }
                    TrafficSelector objectiveSelector = objectiveSelectorBuilder.build();

                    TrafficTreatment dropTreatment = DefaultTrafficTreatment.builder()
                            .drop().build();

                    ForwardingObjective objective = DefaultForwardingObjective.builder()
                            .withSelector(objectiveSelector)
                            .withTreatment(dropTreatment)
                            .fromApp(appId)
                            .withPriority(intent.priority() - 1)
                            .makeTemporary(DROP_RULE_TIMEOUT)
                            .withFlag(ForwardingObjective.Flag.VERSATILE)
                            .add();

                    flowObjectiveService.forward(context.outPacket().sendThrough(), objective);
                } else {
                    log.warn("IntentReactiveForwarding: Intent with key {} failed. " +
                            "No drop rule installed, as same intent is pending.", key.toString());
                }
            }

        } else if (intent == null) {
            // if no intent is present, create a new one
            HostToHostIntent hostIntent = HostToHostIntent.builder()
                    .appId(appId)
                    .key(key)
                    .one(srcId)
                    .two(dstId)
                    .selector(selector)
                    .treatment(treatment)
                    .constraints(Lists.newArrayList(
                            // new LatencyConstraint(Duration.ofNanos(12000)),
                            // new BandwidthConstraint(Bandwidth.mbps(10))))
                            // new AnnotationConstraint("bandwidth", 10_000_000)))
                            // new AdvancedAnnotationConstraint("bandwidth", 200_000, false)
                            ))
                    .build();

            intentService.submit(hostIntent);
        }

    }

}
